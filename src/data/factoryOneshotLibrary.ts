export function getFactoryByCategory(category: string): FactoryOneshotItem[] {
  return FACTORY_ONESHOT_LIBRARY.filter(i => i.category === category);
}

export function getFactoryItemPath(
  item: FactoryOneshotItem,
  sampleLibraryPath: string,
): string {
  // sampleLibraryPath already ends without trailing slash
  return `${sampleLibraryPath}\\${item.relPath.replace(/\//g, '\\')}`;
}
