// ─── Helper ──────────────────────────────────────────────────────────────────
function entry(
  id: string,
  category: string,
  filename: string,
  extraTags: string[] = [],
): FactoryOneshotItem {
  // Strip audio + Kontakt compound extensions (.nksn.ogg, .nksf.ogg, .nki.ogg, .wav, etc.)
  const name = filename.replace(/(\.(nksn|nksf|nki))?(\.(wav|mp3|ogg|aiff?))$/i, '');
  return {
    id,
    name,
    category,
    displayCategory: CATEGORY_DISPLAY[category] ?? category,
    filename,
    relPath: `VSTGOD- God Of Oneshots/${category}/${filename}`,
    tags: [CATEGORY_DISPLAY[category] ?? category, 'Factory', 'VSTGOD', ...extraTags],
  };
}

